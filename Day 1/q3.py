# 30-Day Python Coding Challenge: From Basic to Advanced 

# Day 3: Basic Arithmetic Operations
# Question: Write a program that performs basic arithmetic operations (addition, subtraction, multiplication, division) on two numbers.

# Solution:

num_one=10
num_two=2

# Addition:
print(f"Addition: {num_one + num_two}")

# Subtraction:
print(f"Subtraction: {num_one - num_two}")

# Multiplication:
print(f"Multiplication: {num_one * num_two}")

# Division:
print(f"Division: {num_one / num_two}")

# Integer Division:
print(f"Integer Division: {num_one // num_two}")

# Modulus:
print(f"Modulus: {num_one % num_two}")

# Exponentiation:
print(f"Exponentiation: {num_one ** num_two}")

